char	*do_power_two(int nb_oct)
{
  char	*power_two;

  if (nb_oct == 1)
    power_two = BOUND_1;
  if (nb_oct == 2)
    power_two = BOUND_2;
  if (nb_oct == 3)
    power_two = BOUND_3;
  if (nb_oct == 4)
    power_two = BOUND_4;
  return (power_two);
}
